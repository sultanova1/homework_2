var num1 = prompt('enter number')
switch (num1){
    case num1 = '1':
        console.log('monday')
        break;
    case num1 = '2':
        console.log('tuesday')
        break;
    case num1 = '3':
        console.log('wednesday')
        break;
    case num1 = '4':
        console.log('thursday')
        break;
    case num1 = '5':
        console.log('friday')
        break;
    case num1 = '6':
        console.log('saturday')
        break;
    case num1 = '7':
        console.log('sunday')
        break;
    default:
        console.log('enter a number from 1 to 7')
}
